void light4::initShader()
{
    lightingShader = new Shader;
    lightingShader->AddVertexShader(":/config/Lighting/4/vertex.glsl");
    lightingShader->AddFragmentShader(":/config/Lighting/4/fragment.glsl");
    lightingShader->Link();

    lampgShader = new Shader;
    lampgShader->AddVertexShader(":/config/Lighting/4/lightvertex.glsl");
    lampgShader->AddFragmentShader(":/config/Lighting/4/lightfragment .glsl");
    lampgShader->Link();
}

void light4::renderOver()
{
    lightingShader->setVec3("light.ambient",  QVector3D(0.2f, 0.2f, 0.2f));
    lightingShader->setVec3("light.diffuse",  QVector3D(0.5f, 0.5f, 0.5f)); // 将光照调暗了一些以搭配场景
    lightingShader->setVec3("light.specular", QVector3D(1.0f, 1.0f, 1.0f));


    lightingShader->setVec3("material.specular", QVector3D(0.5f, 0.5f, 0.5f));
    lightingShader->setFloat("material.shininess", 64.0f);
    texture->ActiveTexture();
}
