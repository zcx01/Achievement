uint8_t UTILITY_CRC8_Calculate(uint8_t init_value, const uint8_t data[], uint32_t data_length, bool is_init_enable)
{
    uint8_t result = 0;

    if (is_init_enable == true) 
    {
        result = init_value;
    }
    for (uint32_t i=0; i<data_length; i++) 
    {
        result = Crc8Table[result ^ data[i]];
    }
    return result;
}

//CRC8 x8+x4+x3+x2+1
uint8_t UTILITY_CRC8_Calculate_x84321(uint8_t init_value, const uint8_t *data, uint32_t data_length, bool is_init_enable, bool post_event)
{
    uint8_t result = 0xFF;
    uint8_t crc8_val = 0;
    uint32_t i;

    if (is_init_enable == true) 
    {
        result = init_value;
    }
    for (i = 0; i < data_length; i++)
    {
        result = (uint8_t)(CRC8Table_x84321[(result & 0xFF) ^ data[i]]);
    }

    crc8_val = result;
    if(post_event == true)
    {
        crc8_val = ~crc8_val;
    }

    return (uint8_t)(crc8_val);
}
